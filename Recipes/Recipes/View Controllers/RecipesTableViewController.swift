import UIKit

class RecipesTableViewController: UITableViewController {
    
static let reuseIdentifier = "RecipeCell"
    
    var recipes: [Recipe] = [] {
        didSet {
            DispatchQueue.main.async {
                self.tableView.reloadData()
            }
        }
    }
    
    
    override func viewDidLoad() {
        super.viewDidLoad()

    }

    // MARK: - Table view data source


    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return recipes.count
        
    }
    
    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: RecipesTableViewController.reuseIdentifier, for: indexPath)
        cell.textLabel?.text = recipes[indexPath.row].name
        
        return cell
    }
    
    // MARK: - Navigation
    
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        // Get the new view controller using segue.destination.
        
        if segue.identifier == "ShowRecipeDetail" ,
            let recipeDetailVC = segue.destination as? RecipeDetailViewController,
            let indexPath = self.tableView.indexPathForSelectedRow {
            recipeDetailVC.recipe = recipes[indexPath.row]
            recipeDetailVC.recipeNameLabel?.text = recipes[indexPath.row].name
            recipeDetailVC.recipeTextView?.text = recipes[indexPath.row].name
        }
        
    }

